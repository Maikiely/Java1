    private void jButtonOKActionPerformed(java.awt.event.ActionEvent evt) {                                          
        // TODO add your handling code here:
        Pessoa p = new Pessoa();
        if(jTextFieldSobrenome.getText().isEmpty() || jTextFieldNome.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"O nome e o sobrenome devem ser preenchidos" , "ERRO DE UTILIZACAO", JOptionPane.ERROR_MESSAGE);
        }else{
            try{
                String msg = "Bem Vindo " + jTextFieldNome.getText()+" " +jTextFieldSobrenome.getText();
                JOptionPane.showMessageDialog(rootPane,msg , "BEM VINDO", JOptionPane.INFORMATION_MESSAGE);
            
                p.setNome(jTextFieldNome.getText()); // pega o atributo da classe pessoa e salva no vetor
                p.setSobrenome(jTextFieldSobrenome.getText());
                int idade = Integer.parseInt(jTextFieldIdade.getText()); // faz a conversão de String para inteiro
                p.setIdade(idade);
            
                a.add(p);
           
                br.write(p.toStringToFile()); // escrevendo no arquivo
                br.newLine(); // saltando uma linha
                br.flush();// para salvar os dados no arquibvo e não só no buffer.
                
            } catch (IOException ex) {
                 JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            }
        }
        System.out.println(a);
        //jLabel1Nome.setForeground(Color.blue);
        //jTextFieldNome.setBackground(Color.red);
    }                                         

