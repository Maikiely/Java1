    private void LeituraActionPerformed(java.awt.event.ActionEvent evt) {                                        
        //Criando método para leitura de arquivo.
        try{
            String textFile = new String();
            File f = new File("arquivo.txt");
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            
            String line = br.readLine();
            while(line != null){
                Pessoa p = new Pessoa();
                StringTokenizer st = new StringTokenizer(line,"#"); //separado  
                String nome = st.nextToken();
                String sobrenome = st.nextToken();
                String idade = st.nextToken();
                p.setNome(nome);
                p.setSobrenome(sobrenome);
                int idadeInt = Integer.parseInt(idade);
                p.setIdade(idadeInt);
                
                JOptionPane.showInputDialog(this,p.toString());
               
                textFile = textFile + "\n" + line;
                line = br.readLine();
            }
            JOptionPane.showInputDialog(this,textFile);
        }catch(IOException ex){
            ex.printStackTrace();
        }
        
        
        
    }                                       

